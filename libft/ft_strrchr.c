#include "libft.h"

char	*ft_strrchr(const char *str, int ch)
{
	size_t	a;
	char	*b;

	b = NULL;
	a = 0;
	while (1)
	{
		if (str[a] == ch)
			b = (char *)(str + a);
		if (str[a] == '\0')
			break ;
		++a;
	}
	return (b);
}
