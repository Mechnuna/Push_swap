#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*nxt;

	if (!(lst))
		return (NULL);
	nxt = lst;
	while (nxt->next != NULL)
	{
		nxt = nxt->next;
	}
	return (nxt);
}
