#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	a;

	a = 0;
	if (!(dst) || !(src))
		return (0);
	if (size != 0)
	{
		while (src[a] != '\0' && a < (size - 1))
		{
			dst[a] = src[a];
			a++;
		}
		dst[a] = '\0';
	}
	return (ft_strlen(src));
}
